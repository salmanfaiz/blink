int led0 = 13;
int led1 = 12;
int led2 = 11;
int led3 = 10;
void setup()
{
pinMode(13, OUTPUT);
pinMode(12, OUTPUT);
pinMode(11, OUTPUT);
pinMode(10, OUTPUT);
}
void loop()
{
digitalWrite(led0, HIGH);
delay(666);
digitalWrite(led0. LOW);
delay(666);
{
digitalWrite(led1, HIGH);
delay(666);
digitalWrite(led1, LOW);
delay(666);
}
{
digitalWrite(led2, HIGH);
delay(666);
digitalWrite(led2, LOW);
delay(666)
}
{
digitalWrite(led3, HIGH);
delay(666);
digitalWrite(led3, HIGH);
delay(666);
}
}
